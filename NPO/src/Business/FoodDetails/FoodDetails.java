package Business.FoodDetails;

/**
 *
 * @author Malick
 */
public class FoodDetails {
    private Integer foodPerishableType;
    private Integer foodPreservedType;
    private Integer foodFruitsType;
    private Integer totalQuantity;

    public Integer getFoodPerishableType() {
        return foodPerishableType;
    }

    public void setFoodPerishableType(Integer foodPerishableType) {
        this.foodPerishableType = foodPerishableType;
    }

    public Integer getFoodPreservedType() {
        return foodPreservedType;
    }

    public void setFoodPreservedType(Integer foodPreservedType) {
        this.foodPreservedType = foodPreservedType;
    }

    public Integer getFoodFruitsType() {
        return foodFruitsType;
    }

    public void setFoodFruitsType(Integer foodFruitsType) {
        this.foodFruitsType = foodFruitsType;
    }

    public Integer getTotalQuantity() {
        return totalQuantity;
    }

    public void setTotalQuantity(Integer totalQuantity) {
        this.totalQuantity = totalQuantity;
    }

    

    
}
