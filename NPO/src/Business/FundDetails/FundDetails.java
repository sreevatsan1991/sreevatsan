package Business.FundDetails;

/**
 *
 * @author Malick
 */
public class FundDetails {
    
    private Integer fundEducationType;
    private Integer fundRuralDevelopmentType;
    private Integer fundMedicalTreatmentType;
    private Integer totalQuantity;

    public Integer getFundEducationType() {
        return fundEducationType;
    }

    public void setFundEducationType(Integer fundEducationType) {
        this.fundEducationType = fundEducationType;
    }

    public Integer getFundRuralDevelopmentType() {
        return fundRuralDevelopmentType;
    }

    public void setFundRuralDevelopmentType(Integer fundRuralDevelopmentType) {
        this.fundRuralDevelopmentType = fundRuralDevelopmentType;
    }

    public Integer getFundMedicalTreatmentType() {
        return fundMedicalTreatmentType;
    }

    public void setFundMedicalTreatmentType(Integer fundMedicalTreatmentType) {
        this.fundMedicalTreatmentType = fundMedicalTreatmentType;
    }

    public Integer getTotalQuantity() {
        return totalQuantity;
    }

    public void setTotalQuantity(Integer totalQuantity) {
        this.totalQuantity = totalQuantity;
    }
    
    
    
}
